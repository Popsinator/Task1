package task1.cycle;

/**
 * Объявить переменные всех вам известных типов и вывести их значения на экран.
 */
public class Challenge1 {

    static byte bt;
    static short sh;
    static int in;
    static long ln;
    static float fl;
    static double db;
    static char ch;
    static boolean bl;

    public static void outVariable() {
        System.out.println("Значение по умолчанию для byte " + bt);
        System.out.println("Значение по умолчанию для short " + sh);
        System.out.println("Значение по умолчанию для int " + in);
        System.out.println("Значение по умолчанию для long " + ln);
        System.out.println("Значение по умолчанию для float " + fl);
        System.out.println("Значение по умолчанию для double " + db);
        System.out.println("Значение по умолчанию для char " + ch);
        System.out.println("Значение по умолчанию для boolean " + bl);
    }
}
